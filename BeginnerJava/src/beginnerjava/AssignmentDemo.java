
package beginnerjava;

public class AssignmentDemo {
    
    public static void main(String[] args) {
        
        int x=3,y=2;
        
        x+=y;//x= x+y;
        System.out.println("X = "+x);
        
        x-=y;//x= x-y;
        System.out.println("X = "+x);
        
        x*=y;//x= x*y;
        System.out.println("X = "+x);
        
        x/=y;//x= x/y;
        System.out.println("X = "+x);
        
        x%=y;//x= x%y;
        System.out.println("X = "+x);     
    }    
}
